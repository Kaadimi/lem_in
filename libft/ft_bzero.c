#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*dst;
	int				i;

	dst = s;
	i = 0;
	while (n > 0)
	{
		dst[i] = '\0';
		i++;
		n--;
	}
}
